    val LocalMyClass = compositionLocalOf<String> { error("not ready") }
    CompositionLocalProvider(LocalMyClass provides "airc") {
        content()

       val zhou:String = LocalMyClass.current
    }
}

@Composable
fun GithubTheme(darkTheme: Boolean = true, content: @Composable () -> Unit) {
    val colors = if (darkTheme) {
        DarkColorPalette
    } else {
        LightColorPalette
    }

    MaterialTheme(
        colors = colors,
        typography = Typography,
        shapes = Shapes,
        content = content
    )
}
